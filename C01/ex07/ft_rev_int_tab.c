#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int		xd;
	int		i;
	int		j;

	j = size - 1;
	i = 0;
	while (i < j)
	{
		xd = tab[i];
		tab[i] = tab[j];
		tab[j] = xd;
		j--;
		i++;
	}
}
