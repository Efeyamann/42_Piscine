#include <unistd.h>

void	yb(int a, int b)
{
	char	s1;
	char	s2;

	s1 = '0' + (a / 10);
	s2 = '0' + (a % 10);
	write(1, &s1, 1);
	write(1, &s2, 1);
	write(1, " ", 1);
	s1 = '0' + (b / 10);
	s2 = '0' + (b % 10);
	write(1, &s1, 1);
	write(1, &s2, 1);
}

void	ft_print_comb2(void)
{
	int		a;
	int		b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			yb(a, b);
			if (a != 98 || b != 99)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
