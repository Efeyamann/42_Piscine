#include <unistd.h>

int	ft_strlen(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		j;

	if (*to_find == '\0')
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (str[i] != '\0' && str[i + j] == to_find[j])
		{
			j++;
			if (j == ft_strlen(to_find))
			{
				return (&str[i]);
			}
		}
		i++;
	}
	return (NULL);
}
